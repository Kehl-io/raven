fn build_usage_cost_anomaly(
    runs: &[WorkflowRun],
    period: &str,
    multiplier: f64,
) -> UsageCostAnomaly {
    let now = Utc::now();
    let current_start = usage_window_start(period, now, runs);
    let current_end = now;
    let current_days =
        ((current_end.date_naive() - current_start.date_naive()).num_days() + 1).max(1);
    let baseline_days = if period == "today" {
        7
    } else {
        current_days.max(1)
    };
    let baseline_end = current_start;
    let baseline_start = baseline_end - Duration::days(baseline_days);
    let current_runs = cost_runs_in_window(runs, current_start, current_end);
    let baseline_runs = cost_runs_in_window(runs, baseline_start, baseline_end);
    let current_average_daily_usd = sum_run_cost(&current_runs) / current_days as f64;
    let baseline_average_daily_usd = sum_run_cost(&baseline_runs) / baseline_days as f64;
    let normalized_multiplier = if multiplier.is_finite() && multiplier >= 1.1 {
        multiplier
    } else {
        2.0
    };

    UsageCostAnomaly {
        detected: baseline_average_daily_usd > 0.0
            && current_average_daily_usd > baseline_average_daily_usd * normalized_multiplier,
        period: period.to_string(),
        multiplier: normalized_multiplier,
        current_window_label: format_window_label(current_start, current_end),
        baseline_window_label: format_window_label(baseline_start, baseline_end),
        current_average_daily_usd,
        baseline_average_daily_usd,
        current_run_count: current_runs.len(),
        baseline_run_count: baseline_runs.len(),
        baseline_days,
        source: "sqlite-workflow-runs".to_string(),
    }
}

fn usage_window_start(period: &str, now: DateTime<Utc>, runs: &[WorkflowRun]) -> DateTime<Utc> {
    let today = now.date_naive().and_hms_opt(0, 0, 0).unwrap().and_utc();
    match period {
        "today" => today,
        "7d" => today - Duration::days(6),
        "30d" => today - Duration::days(29),
        "mtd" => NaiveDate::from_ymd_opt(now.year(), now.month(), 1)
            .and_then(|date| date.and_hms_opt(0, 0, 0))
            .map(|date| date.and_utc())
            .unwrap_or(today),
        "all" => runs
            .iter()
            .filter_map(|run| parse_run_started_at(run).ok())
            .min()
            .map(|date| date.date_naive().and_hms_opt(0, 0, 0).unwrap().and_utc())
            .unwrap_or(today),
        _ => today,
    }
}

fn cost_runs_in_window(
    runs: &[WorkflowRun],
    start_inclusive: DateTime<Utc>,
    end_exclusive: DateTime<Utc>,
) -> Vec<&WorkflowRun> {
    runs.iter()
        .filter(|run| run.total_cost_usd.is_some())
        .filter(|run| {
            parse_run_started_at(run)
                .map(|started_at| started_at >= start_inclusive && started_at < end_exclusive)
                .unwrap_or(false)
        })
        .collect()
}

fn parse_run_started_at(run: &WorkflowRun) -> Result<DateTime<Utc>, chrono::ParseError> {
    DateTime::parse_from_rfc3339(&run.started_at).map(|date| date.with_timezone(&Utc))
}

fn sum_run_cost(runs: &[&WorkflowRun]) -> f64 {
    runs.iter()
        .map(|run| run.total_cost_usd.unwrap_or(0.0))
        .sum()
}

fn format_window_label(start: DateTime<Utc>, end: DateTime<Utc>) -> String {
    format!(
        "{:04}-{:02}-{:02}-{:04}-{:02}-{:02}",
        start.year(),
        start.month(),
        start.day(),
        end.year(),
        end.month(),
        end.day(),
    )
}


pub fn approve_workflow_signature_baseline(
    repository: &Repository,
    workflow_id: &str,
    workflow_version: i64,
) -> Result<(), String> {
    let workflow_version = workflow_version_by_identity(repository, workflow_id, workflow_version)?;
    let plugins = plugins::discover_plugins();
    let (definition_signature, runtime_signature, plugin_capabilities) =
        runtime::approval_signature_snapshot(&workflow_version, &plugins);
    if repository
        .last_approved_workflow_signature(workflow_id)
        .map_err(|error| error.to_string())?
        .as_deref()
        == Some(runtime_signature.as_str())
    {
        return Ok(());
    }

    let approved_at = Utc::now().to_rfc3339();
    let run_id = format!("onboarding-baseline-{}", Uuid::new_v4());
    let workflow_id = workflow_version.workflow_id.clone();
    let workflow_version_id = workflow_version.id.clone();
    let workflow_name = workflow_version.definition.name.clone();
    let definition = workflow_version.definition.clone();
    let approval_mode = workflow_version
        .approval_mode
        .clone()
        .unwrap_or_else(|| "review_changes".into());
    let action_steps = workflow_version
        .definition
        .steps
        .iter()
        .map(|step| {
            serde_json::json!({
                "step_id": step.id,
                "provider": step.provider,
                "action": step.action,
                "permissions": step.permissions,
            })
        })
        .collect::<Vec<_>>();
    let payload = serde_json::json!({
        "schema_version": "approval-runtime.v1",
        "runtime_kind": "onboarding",
        "workflow_id": workflow_id,
        "workflow_version_id": workflow_version_id,
        "workflow_version": workflow_version.version,
        "workflow_name": workflow_name.clone(),
        "definition": definition,
        "run_id": run_id.clone(),
        "trigger_kind": "onboarding",
        "idempotency_key": run_id.clone(),
        "policy": {
            "approval_mode": approval_mode,
            "reason": "Onboarding approved the installed workflow baseline for the first live sample.",
            "definition_signature": runtime_signature.clone(),
            "workflow_definition_signature": definition_signature,
            "runtime_signature": runtime_signature,
        },
        "defaults": {
            "llm_profile_ref": workflow_version.definition.defaults.llm_profile_ref,
            "destination_ref": workflow_version.definition.defaults.destination_ref,
        },
        "actions": action_steps,
        "plugin_capabilities": plugin_capabilities,
    });
    let payload_json = serde_json::to_string(&payload).map_err(|error| error.to_string())?;
    repository
        .insert_pending_approval(&PendingApproval {
            id: format!("approval-{}", Uuid::new_v4()),
            run_id,
            step_id: "runtime".into(),
            workflow_name,
            description: "Onboarding approved the current workflow definition and tool permissions for the first live sample.".into(),
            risk_level: "normal".into(),
            payload_json: Some(payload_json.clone()),
            status: "approved".into(),
            created_at: approved_at.clone(),
            resolved_at: Some(approved_at),
            decision_reason: Some("onboarding_baseline".into()),
            payload_at_decision: Some(payload_json),
        })
        .map_err(|error| error.to_string())
}

fn validate_approval_grant(
    repository: &Repository,
    grant: &crate::models::ApprovalGrant,
) -> Result<(), String> {
    let workflow_version = workflow_version_for_grant(repository, grant)?;
    let manifest = preflight_manifest_for_grant_validation(repository, &workflow_version)?;
    let capability = manifest
        .capabilities
        .iter()
        .find(|capability| capability.capability_id == grant.capability_id)
        .ok_or_else(|| {
            format!(
                "capability {} is not available in the current capability registry",
                grant.capability_id
            )
        })?;
    if grant.signature_hash != capability.signature_hash {
        return Err(format!(
            "approval grant signature hash does not match capability {}",
            grant.capability_id
        ));
    }
    validate_grant_scope(grant, &manifest)
}


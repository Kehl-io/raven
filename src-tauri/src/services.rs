pub struct NestWeaverDetection {
    pub binary_path: String,
    pub db_path: Option<String>,
    pub projects: Vec<String>,
}

pub fn detect_nestweaver() -> Result<Option<NestWeaverDetection>, String> {
    let which_output = std::process::Command::new("which")
        .arg("nestweaver")
        .output()
        .map_err(|error| error.to_string())?;

    if !which_output.status.success() {
        return Ok(None);
    }

    let binary_path = String::from_utf8_lossy(&which_output.stdout)
        .trim()
        .to_string();

    let status_output = std::process::Command::new(&binary_path)
        .args(["daemon", "status", "--json"])
        .output();

    let db_path = status_output.ok().and_then(|out| {
        if !out.status.success() {
            return None;
        }
        let json: serde_json::Value = serde_json::from_slice(&out.stdout).ok()?;
        json.get("db_path")
            .and_then(|value| value.as_str())
            .map(str::to_string)
    });

    let mut projects = Vec::new();
    if let Some(ref db) = db_path {
        let list_output = std::process::Command::new(&binary_path)
            .args(["list-projects", "--db", db, "--json"])
            .output();

        if let Ok(out) = list_output {
            if out.status.success() {
                if let Ok(json) = serde_json::from_slice::<serde_json::Value>(&out.stdout) {
                    if let Some(arr) = json.as_array() {
                        for item in arr {
                            if let Some(name) = item
                                .get("name")
                                .or_else(|| item.get("project"))
                                .and_then(|value| value.as_str())
                            {
                                projects.push(name.to_string());
                            }
                        }
                    }
                }
            }
        }
    }

    Ok(Some(NestWeaverDetection {
        binary_path,
        db_path,
        projects,
    }))
}


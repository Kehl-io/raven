fn runtime_visible_registry_snapshot() -> crate::capability_registry::CapabilityRegistrySnapshot {
    let raw_tools = crate::tool_discovery::detect_raw_tools();
    let plugin_manifests = plugins::discover_plugins();
    runtime_visible_registry_snapshot_with_plugins(&raw_tools, &plugin_manifests)
}

fn runtime_visible_registry_snapshot_with_plugins(
    raw_tools: &[crate::models::RawToolInventoryItem],
    plugin_manifests: &[PluginManifest],
) -> crate::capability_registry::CapabilityRegistrySnapshot {
    let mut registry = crate::capability_registry::registry_snapshot_with_raw_tools(raw_tools);
    for plugin in plugin_manifests {
        for step in &plugin.steps {
            registry
                .capabilities
                .push(runtime::plugin_capability_descriptor(&plugin, step));
        }
    }
    registry
        .capabilities
        .sort_by(|left, right| left.id.cmp(&right.id));
    registry
        .capabilities
        .dedup_by(|left, right| left.id == right.id);
    registry.hash = crate::capability_registry::registry_hash(&registry.capabilities);
    registry
}


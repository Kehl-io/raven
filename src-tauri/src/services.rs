fn resolve_approval_with_agent_executor(
    repository: &mut Repository,
    id: &str,
    decision: ApprovalDecision,
    reason: Option<&str>,
    agent_executor: Option<&dyn AgentTaskExecutor>,
) -> Result<Option<PendingApproval>, String> {
    let status = match decision {
        ApprovalDecision::Approved => "approved",
        ApprovalDecision::Rejected => "rejected",
    };
    let (approval, changed) = repository
        .resolve_approval(id, status, reason)
        .map_err(|error| error.to_string())?;
    let Some(approval) = approval else {
        return Ok(None);
    };
    if !changed {
        return Ok(Some(approval));
    }

    match decision {
        ApprovalDecision::Approved => {
            let runtime_kind = approval
                .payload_at_decision
                .as_deref()
                .or(approval.payload_json.as_deref())
                .and_then(|payload| serde_json::from_str::<serde_json::Value>(payload).ok())
                .and_then(|payload| {
                    payload
                        .get("runtime_kind")
                        .and_then(|value| value.as_str())
                        .map(str::to_string)
                })
                .unwrap_or_default();
            if runtime_kind == "deterministic" {
                let version =
                    runtime::approved_workflow_version_for_approval(repository, &approval)
                        .map_err(|error| error.to_string())?;
                complete_deterministic_run_with_version(repository, &approval.run_id, &version)?;
            } else if runtime_kind == "agent_task" {
                if let Some(executor) = agent_executor {
                    let version =
                        runtime::approved_workflow_version_for_approval(repository, &approval)
                            .map_err(|error| error.to_string())?;
                    runtime::run_workflow_with_agent_executor(
                        repository,
                        &version.workflow_id,
                        runtime::RunTrigger::ApprovedResume(approval.run_id.clone()),
                        executor,
                    )
                    .map_err(|error| error.to_string())?;
                } else {
                    runtime::resume_approved_run(repository, &approval)
                        .map_err(|error| error.to_string())?;
                }
            } else {
                runtime::resume_approved_run(repository, &approval)
                    .map_err(|error| error.to_string())?;
            }
        }
        ApprovalDecision::Rejected => {
            runtime::reject_approved_run(repository, &approval, reason)
                .map_err(|error| error.to_string())?;
        }
    }
    repository
        .pending_approval(id)
        .map_err(|error| error.to_string())
}

fn artifact_llm_readiness(repository: &Repository) -> Result<ProviderHealth, String> {
    let profile = repository
        .llm_profile("default-openai")
        .map_err(|error| error.to_string())?;
    let provider_id = profile
        .as_ref()
        .map(|profile| profile.provider_id.as_str())
        .unwrap_or("openai");
    let resolved = repository
        .resolve_llm_credential("default-openai")
        .map_err(|error| error.to_string())?;
    let provider_name = match provider_id {
        "anthropic" => "Anthropic",
        _ => "OpenAI",
    };
    Ok(ProviderHealth {
        id: provider_id.into(),
        name: provider_name.into(),
        kind: ProviderKind::Llm,
        status: if resolved.is_some() {
            ProviderStatus::Available
        } else {
            ProviderStatus::NeedsConfig
        },
        summary: if resolved.is_some() {
            format!("{provider_name} artifact generation is ready.")
        } else {
            format!("{provider_name} artifact generation needs an API key in Settings.")
        },
        fallback_provider_id: None,
    })
}

struct RepositoryAgentCredentialResolver<'a> {
    repository: &'a Repository,
}

impl builder_agent::AgentCredentialResolver for RepositoryAgentCredentialResolver<'_> {
    fn resolve(&self, profile: &AgentAuthProfile) -> Option<String> {
        if let Ok(Some(secret)) = self
            .repository
            .resolve_credential_reference(&profile.credential_ref)
        {
            return Some(secret);
        }

        match profile.runner_kind {
            AgentRunnerKind::OpenAiApi => self
                .repository
                .resolve_llm_credential("default-openai")
                .ok()
                .flatten()
                .map(|resolved| resolved.credential),
            AgentRunnerKind::AnthropicApi => self
                .repository
                .provider_account("anthropic-api-key")
                .ok()
                .flatten()
                .and_then(|account| {
                    self.repository
                        .resolve_credential_reference(&account.credential_ref)
                        .ok()
                        .flatten()
                }),
            AgentRunnerKind::CodexCli
            | AgentRunnerKind::ClaudeCodeCli
            | AgentRunnerKind::OllamaLocal => None,
        }
    }
}


pub fn system_health_diagnostics(
    repository: &Repository,
    scheduler_status: SchedulerStatus,
) -> Result<SystemHealthDiagnostics, String> {
    let state = repository.app_state().map_err(|error| error.to_string())?;
    let providers = check_provider_readiness(repository)?;
    let plugin_manifests = plugins::discover_plugins();

    let provider_counts = providers.iter().fold(
        ProviderDiagnostics {
            total: providers.len(),
            available: 0,
            degraded: 0,
            needs_config: 0,
            unavailable: 0,
        },
        |mut counts, provider| {
            match provider.status {
                ProviderStatus::Available => counts.available += 1,
                ProviderStatus::Degraded => counts.degraded += 1,
                ProviderStatus::NeedsConfig => counts.needs_config += 1,
                ProviderStatus::Unavailable => counts.unavailable += 1,
            }
            counts
        },
    );

    let destinations = providers
        .iter()
        .filter(|provider| provider.kind == ProviderKind::ArtifactDestination)
        .fold(
            DestinationDiagnostics {
                total: 0,
                ready: 0,
                needs_config: 0,
                unavailable: 0,
            },
            |mut counts, provider| {
                counts.total += 1;
                match provider.status {
                    ProviderStatus::Available | ProviderStatus::Degraded => counts.ready += 1,
                    ProviderStatus::NeedsConfig => counts.needs_config += 1,
                    ProviderStatus::Unavailable => counts.unavailable += 1,
                }
                counts
            },
        );

    let mut workflows = WorkflowDiagnostics {
        total: state.workflows.len(),
        enabled: 0,
        draft: 0,
        disabled: 0,
        invalid: 0,
        blocking_issues: 0,
    };
    for version in &state.workflows {
        match version.status {
            WorkflowStatus::Enabled => workflows.enabled += 1,
            WorkflowStatus::Draft => workflows.draft += 1,
            WorkflowStatus::Disabled => workflows.disabled += 1,
        }
        if let Err(error) =
            crate::workflow::validate_workflow_with_plugins(&version.definition, &plugin_manifests)
        {
            workflows.invalid += 1;
            workflows.blocking_issues += count_validation_issues(&error.to_string());
        }
    }

    let runs = state.runs.iter().fold(
        RunDiagnostics {
            total: state.runs.len(),
            failed: 0,
            retryable: 0,
            blocked: 0,
            running: 0,
        },
        |mut counts, run| {
            match run.status {
                RunStatus::Failed => counts.failed += 1,
                RunStatus::Retryable => counts.retryable += 1,
                RunStatus::Blocked => counts.blocked += 1,
                RunStatus::Running | RunStatus::Queued => counts.running += 1,
                RunStatus::Succeeded => {}
            }
            counts
        },
    );

    let plugins = PluginDiagnostics {
        installed: plugin_manifests.len(),
        available_steps: plugin_manifests
            .iter()
            .map(|manifest| manifest.steps.len())
            .sum(),
    };
    let issue_count = usize::from(!scheduler_status.running)
        + provider_counts.degraded
        + provider_counts.needs_config
        + provider_counts.unavailable
        + workflows.blocking_issues
        + runs.failed
        + runs.retryable
        + runs.blocked;

    Ok(SystemHealthDiagnostics {
        generated_at: Utc::now().to_rfc3339_opts(SecondsFormat::Secs, true),
        status: if issue_count == 0 { "ok" } else { "warning" }.into(),
        issue_count,
        scheduler: scheduler_status,
        providers: provider_counts,
        destinations,
        workflows,
        runs,
        plugins,
    })
}

fn count_validation_issues(message: &str) -> usize {
    let count = message
        .split(". ")
        .filter(|issue| !issue.trim().trim_end_matches('.').is_empty())
        .count();
    count.max(1)
}


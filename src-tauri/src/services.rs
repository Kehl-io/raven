fn validate_grant_scope(
    grant: &crate::models::ApprovalGrant,
    manifest: &crate::models::PreflightManifest,
) -> Result<(), String> {
    use crate::models::ApprovalGrantType;
    let capability_id = grant.capability_id.as_str();
    let scope_valid = match grant.grant_type {
        ApprovalGrantType::CredentialUse => {
            let allowed = manifest
                .credentials
                .iter()
                .filter(|item| item.capability_id == capability_id)
                .map(|item| item.credential_ref.clone())
                .collect::<Vec<_>>();
            let credential_ref = grant
                .scope
                .credential_ref
                .as_deref()
                .filter(|value| !value.trim().is_empty());
            (credential_ref.is_some() || !grant.scope.resource_ids.is_empty())
                && credential_ref.is_none_or(|value| allowed.iter().any(|allowed| allowed == value))
                && subset_of_allowed(&grant.scope.resource_ids, &allowed)
        }
        ApprovalGrantType::FileWrite | ApprovalGrantType::FileOverwrite => {
            if grant.grant_type == ApprovalGrantType::FileOverwrite
                && !grant
                    .scope
                    .max_overwrite_bytes
                    .is_some_and(|limit| limit > 0)
            {
                return Err(
                    "file overwrite grants require max_overwrite_bytes greater than zero".into(),
                );
            }
            let mut allowed = scoped_values_for_capability(
                if grant.grant_type == ApprovalGrantType::FileOverwrite {
                    &manifest.scoped_overwrites
                } else {
                    &manifest.scoped_file_writes
                },
                capability_id,
            );
            if grant.grant_type == ApprovalGrantType::FileWrite {
                let overwrites =
                    scoped_values_for_capability(&manifest.scoped_overwrites, capability_id);
                allowed.retain(|path| !overwrites.iter().any(|overwrite| overwrite == path));
            }
            !grant.scope.paths.is_empty() && subset_of_allowed(&grant.scope.paths, &allowed)
        }
        ApprovalGrantType::FileDelete => {
            let deletes = manifest
                .deletes
                .iter()
                .filter(|item| item.capability_id == capability_id)
                .collect::<Vec<_>>();
            let allowed_paths = deletes
                .iter()
                .map(|item| item.path_pattern.clone())
                .collect::<Vec<_>>();
            let max_allowed_deletes = deletes.iter().filter_map(|item| item.max_deletes).max();
            let max_deletes_valid = match max_allowed_deletes {
                Some(max_allowed) => grant.scope.max_deletes.is_some_and(|requested| {
                    requested <= max_allowed && requested >= grant.scope.paths.len() as u64
                }),
                None => true,
            };
            !grant.scope.paths.is_empty()
                && grant.scope.resource_ids.is_empty()
                && subset_of_allowed(&grant.scope.paths, &allowed_paths)
                && max_deletes_valid
        }
        ApprovalGrantType::NetworkAccess => {
            let allowed_domains =
                scoped_values_for_capability(&manifest.scoped_network_domains, capability_id);
            let allowed_resources =
                scoped_values_for_capability(&manifest.scoped_network_resources, capability_id);
            (!grant.scope.domains.is_empty() || !grant.scope.resource_ids.is_empty())
                && subset_of_allowed(&grant.scope.domains, &allowed_domains)
                && subset_of_allowed(&grant.scope.resource_ids, &allowed_resources)
        }
        ApprovalGrantType::ExternalPublish => {
            let allowed =
                scoped_values_for_capability(&manifest.scoped_external_publishes, capability_id);
            !grant.scope.external_targets.is_empty()
                && subset_of_allowed(&grant.scope.external_targets, &allowed)
        }
        ApprovalGrantType::ToolExecution => {
            manifest
                .capabilities
                .iter()
                .any(|capability| capability.capability_id == capability_id)
                && scoped_values_for_capability(&manifest.scoped_file_writes, capability_id)
                    .is_empty()
                && scoped_values_for_capability(&manifest.scoped_overwrites, capability_id)
                    .is_empty()
                && scoped_values_for_capability(&manifest.scoped_network_domains, capability_id)
                    .is_empty()
                && scoped_values_for_capability(&manifest.scoped_network_resources, capability_id)
                    .is_empty()
                && scoped_values_for_capability(&manifest.scoped_external_publishes, capability_id)
                    .is_empty()
                && !manifest
                    .credentials
                    .iter()
                    .any(|item| item.capability_id == capability_id)
                && !manifest
                    .deletes
                    .iter()
                    .any(|item| item.capability_id == capability_id)
        }
    };
    if scope_valid {
        Ok(())
    } else {
        Err(format!(
            "approval grant {:?} scope is not required or exceeds preflight bounds",
            grant.grant_type
        ))
    }
}

fn scoped_values_for_capability(
    items: &[crate::models::PreflightScopedValueUse],
    capability_id: &str,
) -> Vec<String> {
    let mut values = items
        .iter()
        .filter(|item| item.capability_id == capability_id)
        .map(|item| item.value.clone())
        .collect::<Vec<_>>();
    values.sort();
    values.dedup();
    values
}

fn subset_of_allowed(requested: &[String], allowed: &[String]) -> bool {
    requested
        .iter()
        .all(|value| !value.trim().is_empty() && allowed.iter().any(|allowed| allowed == value))
}


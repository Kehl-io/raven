pub fn available_capability_catalog_with_category_overrides(
    mode: crate::autonomy::AutonomyMode,
    category_overrides: &crate::autonomy::CategoryAutonomyOverrides,
) -> crate::capability_registry::CapabilityRegistrySnapshot {
    let mut snapshot = runtime_visible_registry_snapshot();
    let mut visible_capabilities = Vec::new();
    let mut policy_decisions = Vec::new();
    for capability in snapshot.capabilities {
        let decision = crate::autonomy::evaluate_capability_policy_with_overrides(
            &capability,
            mode,
            category_overrides,
        );
        if decision.decision != crate::autonomy::PolicyDecisionKind::Hidden {
            visible_capabilities.push(capability);
            policy_decisions.push(decision);
        }
    }
    snapshot.capabilities = visible_capabilities;
    snapshot.policy_decisions = policy_decisions;
    snapshot
}


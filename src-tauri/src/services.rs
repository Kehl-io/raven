pub fn run_workflow_deterministic(
    repository: &mut Repository,
    workflow_id: &str,
) -> Result<WorkflowRunResult, String> {
    let version = repository
        .latest_workflow_version(workflow_id)
        .map_err(|error| error.to_string())?
        .ok_or_else(|| format!("workflow {workflow_id} was not found"))?;
    if version.status != WorkflowStatus::Enabled {
        return Err(format!(
            "workflow {workflow_id} is {} and cannot be run",
            format!("{:?}", version.status).to_lowercase()
        ));
    }
    let plugin_manifests = plugins::discover_plugins();
    if version.definition.steps.iter().any(|step| {
        plugins::plugin_for_step(&plugin_manifests, &step.provider, &step.action).is_some()
    }) {
        return runtime::run_workflow_with_plugins_and_event_sink(
            repository,
            workflow_id,
            runtime::RunTrigger::Manual,
            &plugin_manifests,
            &crate::stream::NoopRuntimeEventSink,
        )
        .map_err(|error| error.to_string());
    }
    if version
        .definition
        .steps
        .iter()
        .any(|step| step.kind == crate::models::WorkflowStepKind::AgentTask)
    {
        return runtime::run_workflow_with_agent_executor(
            repository,
            workflow_id,
            runtime::RunTrigger::Manual,
            &DeterministicAgentTaskExecutor,
        )
        .map_err(|error| error.to_string());
    }
    let started_at = Utc::now().to_rfc3339();
    let run = WorkflowRun {
        id: format!("run-{}", Uuid::new_v4()),
        workflow_id: workflow_id.into(),
        workflow_name: version.definition.name.clone(),
        status: RunStatus::Running,
        started_at: started_at.clone(),
        completed_at: None,
        failure_reason: None,
        idempotency_key: format!("manual:{workflow_id}:{}", Uuid::new_v4()),
        trigger_kind: "manual".into(),
        retry_count: 0,
        parent_run_id: None,
        error_classification: None,
        provider_profile_id: Some("deterministic-test-server".into()),
        blocked_reason: None,
        required_provider_id: None,
        required_profile_id: None,
        setup_action: None,
        total_tokens: None,
        input_tokens: None,
        output_tokens: None,
        total_cost_usd: None,
    };
    let step_runs = version
        .definition
        .steps
        .iter()
        .map(|step| WorkflowStepRun {
            id: format!("step-run-{}", Uuid::new_v4()),
            workflow_run_id: run.id.clone(),
            step_id: step.id.clone(),
            status: RunStatus::Running,
            output_json: None,
            error: None,
            started_at: started_at.clone(),
            completed_at: None,
        })
        .collect::<Vec<_>>();

    repository
        .create_run_with_steps(&run, &step_runs)
        .map_err(|error| error.to_string())?;

    if let Some(result) =
        runtime::maybe_pause_for_approval(repository, &version, &run, &step_runs, "deterministic")
            .map_err(|error| error.to_string())?
    {
        return Ok(result);
    }

    complete_deterministic_run_with_version(repository, &run.id, &version)
}

pub fn run_workflow_deterministic_streamed(
    repository: &mut Repository,
    workflow_id: &str,
) -> Result<StreamedWorkflowRunResult, String> {
    let version = repository
        .latest_workflow_version(workflow_id)
        .map_err(|error| error.to_string())?
        .ok_or_else(|| format!("workflow {workflow_id} was not found"))?;
    let plugin_manifests = plugins::discover_plugins();
    if version.definition.steps.iter().any(|step| {
        plugins::plugin_for_step(&plugin_manifests, &step.provider, &step.action).is_some()
    }) {
        let sink = crate::stream::EventLog::default();
        let result = runtime::run_workflow_with_plugins_and_event_sink(
            repository,
            workflow_id,
            runtime::RunTrigger::Manual,
            &plugin_manifests,
            &sink,
        )
        .map_err(|error| error.to_string())?;
        return Ok(StreamedWorkflowRunResult {
            result,
            events: sink.events(),
        });
    }
    let result = run_workflow_deterministic(repository, workflow_id)?;
    let mut events = vec![AgentEvent::RunStarted {
        run_id: result.run.id.clone(),
        thread_id: result.run.id.clone(),
        workflow_name: result.run.workflow_name.clone(),
        timestamp: result.run.started_at.clone(),
    }];

    if result.run.status == RunStatus::Blocked {
        if let Some(approval) = repository
            .pending_approval_for_run(&result.run.id)
            .map_err(|error| error.to_string())?
        {
            events.push(AgentEvent::Interrupt {
                run_id: approval.run_id,
                step_id: approval.step_id,
                approval_id: approval.id,
                workflow_name: approval.workflow_name,
                description: approval.description,
                risk_level: approval.risk_level,
                timestamp: approval.created_at,
            });
        }
        return Ok(StreamedWorkflowRunResult { result, events });
    }

    let step_runs = repository
        .workflow_step_runs_for_run(&result.run.id)
        .map_err(|error| error.to_string())?;
    if let Some(step) = step_runs.first() {
        events.push(AgentEvent::StepStarted {
            run_id: result.run.id.clone(),
            step_id: step.step_id.clone(),
            step_name: deterministic_step_name(&step.step_id),
            timestamp: step.started_at.clone(),
        });
        events.push(AgentEvent::ThinkingContent {
            run_id: result.run.id.clone(),
            step_id: step.step_id.clone(),
            content: "Preparing deterministic provider fixture.".into(),
        });
        events.push(AgentEvent::ToolCallStart {
            run_id: result.run.id.clone(),
            step_id: step.step_id.clone(),
            tool_call_id: format!("{}-deterministic-tool", step.step_id),
            tool_name: "deterministic.provider".into(),
            args: serde_json::json!({ "workflow_id": workflow_id }),
        });
        events.push(AgentEvent::ToolCallEnd {
            run_id: result.run.id.clone(),
            step_id: step.step_id.clone(),
            tool_name: "deterministic.provider".into(),
            result: "fixture ready".into(),
            duration_ms: 0,
        });
        events.push(AgentEvent::StepFinished {
            run_id: result.run.id.clone(),
            step_id: step.step_id.clone(),
            duration_ms: 0,
            token_count: None,
            estimated_cost_usd: None,
        });
    }
    if let Some(step) = step_runs.get(1) {
        events.push(AgentEvent::StepStarted {
            run_id: result.run.id.clone(),
            step_id: step.step_id.clone(),
            step_name: deterministic_step_name(&step.step_id),
            timestamp: step.started_at.clone(),
        });
        events.push(AgentEvent::TextMessageContent {
            run_id: result.run.id.clone(),
            step_id: step.step_id.clone(),
            content: result
                .artifact
                .as_ref()
                .map(|artifact| artifact.content_markdown.clone())
                .unwrap_or_default(),
        });
        events.push(AgentEvent::StepFinished {
            run_id: result.run.id.clone(),
            step_id: step.step_id.clone(),
            duration_ms: 0,
            token_count: Some(16),
            estimated_cost_usd: Some(0.0),
        });
    }
    events.push(AgentEvent::RunFinished {
        run_id: result.run.id.clone(),
        artifact_id: result.artifact.as_ref().map(|artifact| artifact.id.clone()),
        duration_ms: 0,
        token_count: Some(16),
        estimated_cost_usd: Some(0.0),
    });

    Ok(StreamedWorkflowRunResult { result, events })
}

fn deterministic_step_name(step_id: &str) -> String {
    step_id
        .split('-')
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                Some(first) => first.to_uppercase().collect::<String>() + chars.as_str(),
                None => String::new(),
            }
        })
        .collect::<Vec<_>>()
        .join(" ")
}

fn complete_deterministic_run_with_version(
    repository: &mut Repository,
    run_id: &str,
    version: &WorkflowVersion,
) -> Result<WorkflowRunResult, String> {
    let run = repository
        .workflow_run(run_id)
        .map_err(|error| error.to_string())?
        .ok_or_else(|| format!("run {run_id} was not found"))?;
    if let Some(artifact) = repository
        .artifact_for_run(run_id)
        .map_err(|error| error.to_string())?
    {
        return Ok(WorkflowRunResult {
            run,
            artifact: Some(artifact),
            duplicate: true,
        });
    }
    let step_runs = repository
        .workflow_step_runs_for_run(run_id)
        .map_err(|error| error.to_string())?;

    for step in &step_runs {
        repository
            .finish_step(
                &step.id,
                serde_json::json!({
                    "mode": "deterministic",
                    "step_id": step.step_id,
                    "summary": "Generated by the Raven deterministic test server."
                }),
            )
            .map_err(|error| error.to_string())?;
    }

    let artifact_id = format!("artifact-{}", Uuid::new_v4());
    let (content_path, metadata_path) = repository.artifact_paths(&artifact_id);
    let artifact = Artifact {
        id: artifact_id,
        title: format!("{} Test Artifact", version.definition.name),
        artifact_type: "deterministic_test".into(),
        workflow_run_id: run.id.clone(),
        content_path,
        metadata_path,
        content_markdown: format!(
            "# {}\n\nGenerated by the deterministic Raven test server for workflow `{}`.",
            version.definition.name, version.workflow_id
        ),
        metadata: serde_json::json!({
            "schema_version": "0.1.0",
            "workflow_id": version.workflow_id,
            "workflow_version": version.version,
            "provider": "deterministic-test-server",
            "destination_ref": version.definition.defaults.destination_ref
        }),
        source_refs: vec!["raven-test-server:deterministic".into()],
        created_at: Utc::now().to_rfc3339(),
    };
    repository
        .write_artifact(&artifact)
        .map_err(|error| error.to_string())?;
    if version.definition.defaults.destination_ref != "local_app"
        && version.definition.defaults.destination_ref != "local-app"
        && !version.definition.defaults.destination_ref.is_empty()
    {
        repository
            .export_artifact_to_destination(
                &artifact.id,
                &version.definition.defaults.destination_ref,
            )
            .map_err(|error| error.to_string())?;
    }
    repository
        .finish_run_with_totals(&run.id, RunStatus::Succeeded, None, Some(16), Some(0.0))
        .map_err(|error| error.to_string())?;

    Ok(WorkflowRunResult {
        run: WorkflowRun {
            status: RunStatus::Succeeded,
            completed_at: Some(Utc::now().to_rfc3339()),
            total_tokens: Some(16),
            input_tokens: None,
            output_tokens: None,
            total_cost_usd: Some(0.0),
            ..run
        },
        artifact: Some(artifact),
        duplicate: false,
    })
}


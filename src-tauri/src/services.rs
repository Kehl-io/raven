fn deterministic_agent_dependency_output(request: &AgentTaskRequest) -> serde_json::Value {
    let Some(agent_step) = request
        .workflow
        .steps
        .iter()
        .find(|step| step.id == request.step_id)
    else {
        return serde_json::json!({});
    };
    let transform_dependencies = agent_step
        .depends_on
        .iter()
        .filter(|dependency_id| {
            request.workflow.steps.iter().any(|step| {
                step.id == **dependency_id
                    && step.provider == "data"
                    && step.action == "transform_json"
            })
        })
        .collect::<Vec<_>>();
    let dependencies = if transform_dependencies.is_empty() {
        agent_step.depends_on.iter().collect::<Vec<_>>()
    } else {
        transform_dependencies
    };

    let mut output = serde_json::Map::new();
    for dependency_id in dependencies {
        if let Some(value) = request.prior_step_outputs.get(dependency_id) {
            output.insert(dependency_id.clone(), value.clone());
        }
    }
    serde_json::Value::Object(output)
}

struct DeterministicAgentTaskExecutor;

impl AgentTaskExecutor for DeterministicAgentTaskExecutor {
    fn requires_profile_check(&self) -> bool {
        false
    }
    fn execute(&self, request: &AgentTaskRequest) -> Result<AgentTaskEnvelope, AgentTaskError> {
        let dependency_output = deterministic_agent_dependency_output(request);
        let dependency_markdown = serde_json::to_string_pretty(&dependency_output)
            .unwrap_or_else(|_| dependency_output.to_string());
        Ok(AgentTaskEnvelope {
            title: format!("{} Test Artifact", request.workflow.name),
            content_markdown: format!(
                "# {}\n\nGenerated by the deterministic Raven test server for workflow `{}`.\n\n```json\n{}\n```",
                request.workflow.name, request.workflow.id, dependency_markdown
            ),
            metadata: serde_json::json!({
                "kind": "agent_task",
                "mode": "deterministic",
                "estimated_cost_usd": 0.0,
                "workflow_id": request.workflow.id,
                "profile_id": request.profile.id,
                "usage": {
                    "total_tokens": 16
                }
            }),
            source_refs: vec!["raven-test-server:deterministic-agent".into()],
            tool_trace: vec![ToolTraceEntry {
                tool_id: "deterministic.agent".into(),
                status: "succeeded".into(),
                input_summary: serde_json::json!({
                    "step_id": request.step_id,
                    "objective": request.objective
                }),
                output_summary: Some(serde_json::json!({
                    "summary": "deterministic agent fixture"
                })),
                source_refs: vec!["raven-test-server:deterministic-agent".into()],
                error: None,
            }],
            raw_result_json: serde_json::json!({
                "mode": "deterministic",
                "workflow_id": request.workflow.id,
                "dependency_output": dependency_output
            }),
        })
    }
}

